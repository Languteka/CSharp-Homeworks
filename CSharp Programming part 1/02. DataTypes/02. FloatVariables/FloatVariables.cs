using System;

class FloatVariables
{
    static void Main()
    {
        double valOne = 34.567839023;
        float valTwo = 12.345f;
        double valThree = 8923.1234857;
        decimal valFour = 3456.091124875956542151256683467m;
        Console.WriteLine("{0}\n{1}\n{2}\n{3}\n", valOne, valTwo, valThree, valFour);
    }
}

